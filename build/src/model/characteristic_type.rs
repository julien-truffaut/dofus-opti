impl CharacteristicType {
    pub fn localized(&self, language: Language) -> &str {
        match self {
            CharacteristicType::AbilityPoint => match language {
                Language::English => "AP",
                Language::French => "PA",
            },
            CharacteristicType::Power => match language {
                Language::English => "Power",
                Language::French => "Puissance",
            },
            CharacteristicType::Strength => match language {
                Language::English => "Strength",
                Language::French => "Force",
            },
            CharacteristicType::Vitality => match language {
                Language::English => "Vitality",
                Language::French => "VitalitÃ©",
            },
            _ => "Not Defined",
        }
    }
}
